package com.htw.news;

public class NewsBean {
    /**
     * 新闻标题
     */
    public String mTitle = null;
    /**
     * 新闻图片
     */
    public String mPicrure = null;
    /**
     * 新闻来源
     */
    public String mFrom = null;
    /**
     * 新闻时间
     */
    public String mTime = null;
    /**
     * 新闻热度
     */
    public String mHeat = null;
    /**
     * 优先级
     */
    public String mPriority = null;
    /**
     * 新闻链接
     */
    public String mUrl = null;

    public String getTitle() {
        return mTitle;
    }

    public void setTitle(String mTitle) {
        this.mTitle = mTitle;
    }

    public String getPicrure() {
        return mPicrure;
    }

    public void setPicrure(String mPicrure) {
        this.mPicrure = mPicrure;
    }

    public String getFrom() {
        return mFrom;
    }

    public void setFrom(String mFrom) {
        this.mFrom = mFrom;
    }

    public String getTime() {
        return mTime;
    }

    public void setTime(String mTime) {
        this.mTime = mTime;
    }

    public String getHeat() {
        return mHeat;
    }

    public void setHeat(String mHeat) {
        this.mHeat = mHeat;
    }

    public String getPriority() {
        return mPriority;
    }

    public void setPriority(String mPriority) {
        this.mPriority = mPriority;
    }

    public String getUrl() {
        return mUrl;
    }

    public void setUrl(String mUrl) {
        this.mUrl = mUrl;
    }
}
